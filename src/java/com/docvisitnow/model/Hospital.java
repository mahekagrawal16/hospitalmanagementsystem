package com.docvisitnow.model;

/**
 *
 * @author mahekagrawal
 */
public class Hospital {
    private int hospitalId;
    private String name;
    private String type;
    private String logoPath;
    private String address;
    private String city;
    private String contactEmail;
    private String phoneNumber;
    private int adminUserId;
    private String affiliatedDoctors;

    public int getHospitalId() {
        return hospitalId;
    }

    public void setHospitalId(int hospitalId) {
        this.hospitalId = hospitalId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getLogoPath() {
        return logoPath;
    }

    public void setLogoPath(String logoPath) {
        this.logoPath = logoPath;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getContactEmail() {
        return contactEmail;
    }

    public void setContactEmail(String contactEmail) {
        this.contactEmail = contactEmail;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public int getAdminUserId() {
        return adminUserId;
    }

    public void setAdminUserId(int adminUserId) {
        this.adminUserId = adminUserId;
    }

    public String getAffiliatedDoctors() {
        return affiliatedDoctors;
    }

    public void setAffiliatedDoctors(String affiliatedDoctors) {
        this.affiliatedDoctors = affiliatedDoctors;
    }
    
    
}
